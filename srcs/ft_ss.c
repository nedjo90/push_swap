#include "push_swap.h"

void	ft_ss(t_ps **list_a, t_ps **list_b)
{
	if (!*list_a && !*list_b)
		return ;
	ft_swap_head(list_a);
	ft_swap_head(list_b);
	write(1, "ss\n", 3);
}
