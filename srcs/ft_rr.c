#include "push_swap.h"

void	ft_rr(t_ps **list_a, t_ps **list_b)
{
	if (*list_a && *list_b)
	{
		ft_rotate(list_a);
		ft_rotate(list_b);
		write(1, "rr\n", 3);
	}
}
